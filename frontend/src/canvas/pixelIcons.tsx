export type PixelIconName = keyof typeof ICONS;

export function PixelIcon({
  name,
  size = 16,
  title,
}: {
  name: PixelIconName;
  size?: number;
  title?: string;
}) {
  return (
    <svg
      className="px-icon"
      width={size}
      height={size}
      viewBox="0 0 16 16"
      shapeRendering="crispEdges"
      role={title ? "img" : "presentation"}
      aria-label={title}
      aria-hidden={title ? undefined : true}
    >
      {ICONS[name].map(([x, y, w, h], index) => (
        <rect
          key={index}
          x={x}
          y={y}
          width={w}
          height={h}
          fill="currentColor"
        />
      ))}
    </svg>
  );
}
